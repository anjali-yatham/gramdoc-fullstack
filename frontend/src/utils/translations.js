export const getLang = () => {
  if (typeof localStorage === 'undefined') return 'en'
  return localStorage.getItem('gramdoc_lang') || 'en'
}

export const getT = (langOverride) => {
  const lang = langOverride || getLang()
  return LANGS[lang]?.ui || LANGS.en.ui
}

export const getLangCode = (langOverride) => {
  const lang = langOverride || getLang()
  return { en: 'en-IN', hi: 'hi-IN', te: 'te-IN' }[lang] || 'en-IN'
}
